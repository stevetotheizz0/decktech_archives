            <center><h2>Welcome to the DeckTech.net Heroclix Theme Section</h2></center>
<h3>Introduction</h3>
<p>Many of us over here at DeckTech are dedicated tournament players AND fans of the comics that inspired the game.  Its often that we hear complaints about the tournament scene being 'cheesy', and many of these complaints come about because the types of teams that do well in tournaments don't mimic comic books very well.</p>

<p>Well, we put some of our best minds to the task and have developed a style of playing Heroclix that will create tactical situations that mirror the comic book stories of our favorite characters.  We hope you enjoy trying this style of play as much as we had developing it!</p>

<h3>Background</h3>
<p>At its simplest level, comic book battles come about because a bad guy or a team of bad guys is doing something bad, and the hero or heroes stop them.  This can be - breaking into the villain's secret headquarters to thwart their plans, getting to the park and stopping a rampage, rescuing a hostage, stopping a robbery.  You get the idea.</p>

<p>Contrast this with Heroclix tournaments, which assume a handful of random characters have gotten together to have a good-old, knock-down, drag-out brawl in the middle of the board.  The reason many figures perform poorly in Heroclix tournaments is because they don't have powers and statistics that are optimized for these 'last man standing' style of brawls that define standard heroclix play.</p>

<h3>Basic Concept</h3>
<p>The scenarios we've created allow you to build a themed villain team to carry out a specific 'mission'.  Most of the scenarios have time limits and victory conditions; if the heroes don't stop the villains in time, the villains win the game.  While its useful to KO your opponent's figures, in scenario play, you can be ahead on points and still lose the game if you don't stop the other side!</p>

<p>The heroes don't have a scenario.  The hero mission is simple:  stop the villains!  Many of the scenarios will have you trying to rescue a bystander or destroy a device guarded by the villains, putting the hero team on the offensive and the villains on defense.  This is balanced by the fact that hero teams have high mobility and leadership - try it and you'll agree.</p>

<h3>How Do I Start?</h3>
<p>To play a theme night, each player needs both a hero and a villain team for the agreed upon build total.  Use our list of allowable themes to determine which figures are allowed on a given theme.  No more mixing themes, for you true believers and purists!</p>

<p>When you build your villain theme, make sure you pick a scenario for the villains, as well as a map.  <i>The villain player gets to determine the map.</i></p>

<p><b>Example:</b><br>
For scenario night, I choose to play the X-Men for my hero team, and build a 300pt X-Men team.  For the villains, I choose the 'Hostage' scenario, and build a 300pt Sinister Syndicate team (Concrete Jungle theme).  I pick the shopping mall as my map.  Just in case the hero player forgets to bring any extra bystander tokens, I make sure I have a token to be 'the hostage'.</p>
<p>When we sit down to play, my first opponent is playing the Doomsday Device scenario, and built a Red Skull/Hydra theme team.  He chooses the warehouse map. Can the X-Men destroy the Doomsday Device in time?  When we play a rematch, we switch sides, and suddenly his Avengers team have to come to the shopping mall and see if they can rescue the bystander (Jarvis) from the Sinister Syndicate.  Believe me, these scenarios are FUN.</p>

<h3>Playing a Scenario Game</h3>
<p>Player's roll dice to determine who plays villains and who plays the hero team.  The villain team declares their scenario and the map.  The hero player gets to pick their starting area on the map and then the two sides set up.  Initiative is determined by the scenario.  Many scenarios have one side or the other starting in an unusual location and so its important to know that sometimes the scenario will let the villains go first, other times the heroes go first.</p>

<p>All of the scenarios have time limits, typically 10 rounds.  A round consists of both the hero and the villain player getting their turn, and then it becomes the next round.  All of the scenarios have victory conditions listed to determine the winner.</p>

<h3>How to Play a Tournament or League</h3>
<p>It is possible to play a scenario tournament, but the tournament will need to be a minimum of 4 games.  Here's how it works:</p>

<p>For Game 1, pairings are determined normally.  The players determine hero/villain assignments with a dice roll.  When they record their score, they also mark whether they played Heroes or Villains.  They also record the winner/loser (according to the scenario victory conditions) and the KO points.</p>

<p>For Game 2, the judge needs to make two piles of scorecards.  One pile is villain players, the other pile is hero players.  They are arranged in order of Wins and KO Points.  The top scoring villain player will get matched to the top scoring hero player, and so on.  For Game 2, players play the opposite allegiance they played in Game 1.</p>

<p>Now that both players have played Heroes and Villains, Game 3 is similar to Game 1.  Pairings are established normally and the players determine Hero/Villain allegiance with a dice roll.  The pairings for Game 4 are similar to Game 2; the top Hero and Villain players get paired with each other, and they swap allegiances for this game.</p>

<p>Pretty simple!</p>


<h3>New Bystander Rules</h3>
<p>We needed to modify the bystander token rules to reflect the new style of play.  Bystanders are innocent people, and if a scenario calls for the use of bystanders, they are considered to be part of the heroes force once the game begins.  They count towards the heroes allotment of actions for the turn and move as part of the hero force.  Bystanders called into play by the scenario do not count towards the build total for the hero team (but a hero player can still put a bystander on their team as part of their force).  Any of these rules can be changed by specific rules in the scenario.</p>

<p>Captured bystanders move with the capturing figure.  Like the standard rules, the two figures must be adjacent, and move at the slowest speed of the two figures.  Captured bystanders can be freed by a successful close or ranged combat attack against the captor.  The captor takes damage from the attack normally in addition to releasing the hostage.</p>

<p>If a captor is targeted by a ranged attack, the captured bystander adds +2 defense to the captor.  This represents the fact that the heroes are trying not to hit the bystander token.  Bystanders still block line of sight if they are placed in front of the captor.</p>

<p>As long as the captor has a captive bystander token, the captor cannot make any close or ranged attack.</p>
