                  <p><font size="3"><b>Questions and Answers:<br>
                    <br>
                    </b></font> <b><font color="#000066"><a name="deciphercon"></a>What 
                    happened to DecipherCon?</font></b><br>
                    Warren Holland the CEO of Decipher Inc, canceled DecipherCon 
                    out of concern for player safety in light of the recent terrorist 
                    activity. We had the opportunity to talk with him for about 
                    an hour to understand his position more clearly. Nothing in 
                    our conversation indicated there was any other reason for 
                    canceling DecipherCon. Though we understand his concern, we 
                    disagree with his conclusion. <b><font color="#000066"><br>
                    <br>
                    </font></b> FreedomCon is a convention organized and paid 
                    for by players that feel the best way to defeat terrorism 
                    is not by letting it demolish the traditions and institutions 
                    of our community but continuing those traditions defiantly. 
                    <b><font color="#000066"> <br>
                    <br>
                    <a name="travel"></a>So is it safe to travel or not? </font></b></p>
                  <p><b>Quick Answer:</b> Experts agree that air travel is safer 
                    now than it has ever been. However, it's up to you to decide 
                    for yourself and your children. </p>
                  <p><b>Complete Answer:</b> There is always risk in travel. If 
                    you go out in your car there is a risk of getting into a car 
                    accident. But which one of us doesn't get in our car and drive 
                    to tournaments on the weekends? The truth is travel of any 
                    sort has risk, but we are willing to take those risk because 
                    the odds are highly in our favor that everything will turn 
                    out fine. </p>
                  <p>Not everyone agrees on what risk are acceptable. Some people 
                    are afraid to travel, so much so that they never leave their 
                    house. Locked in their own house they become a prisoner to 
                    their own fear. Others are afraid to fly, thus only travel 
                    by bus, car or train. Most, however, adapt to the risk and 
                    learn to accept them and live normal lives, traveling freely. 
                    In light of the recent terrorist attacks is travel more dangerous? 
                    The facts tell us that travel is actually safer. Even still, 
                    you must make the decisions about what risk are acceptable 
                    when making your own travel arrangements. </p>
                  <p><b>Note to Parents: </b>We encourage parents to be actively 
                    involved in their children's life by participating in the 
                    activities that surround the collectible card games they play. 
                    As such, parents are welcome to attend FreedomCom with their 
                    children and take part in some of the many side events at 
                    the convention. Whether or not you decide to attend with your 
                    child, please only allow your child to travel to FreedomCon 
                    if you feel that such travel would be safe for your child, 
                    and then only with the supervision of an adult you trust. 
                  </p>
                  <p><b><font color="#000066"><a name="safe"></a>Is the event 
                    safe from terrorist attacks? </font></b></p>
                  <p><b>Quick Answer:</b> We don't know the future, but nothing 
                    about the venue or event date increases the probability of 
                    a terrorist attack. </p>
                  <p><b>Complete Answer: </b>It is important to realize that the 
                    standard method of operation for terrorist is to use minimal 
                    resources to inflict maximum damage. As such their preferred 
                    targets are large unguarded civilian populations or locations 
                    that are symbolic (The Eiffel tower, Pentagon, etc.). Occasionally 
                    when neither is present terrorist will attack military targets, 
                    such as the USS Cole. </p>
                  <p>By following their standard MO we can expect that any additional 
                    terrorist attack on US soil will target large events where 
                    multiple-thousands of people will be gathered (sporting events, 
                    concerts, or a large building). DecipherCon was projected 
                    to draw approximately 300-400 gamers plus staff. At that size, 
                    the odds of being considered a direct target for terrorist 
                    attacks are extremely low. In fact, there are many places 
                    in the Virginia Beach area alone that hold more people in 
                    one place. </p>
                  <p>Pembroke Mall with its Regal Cinema located in Virginia Beach 
                    routinely boast consumer populations of 2-4 thousand people 
                    on weekends. Furthermore, there are hundreds of other cities 
                    in the U.S. that have larger civilian populations than Virginia 
                    Beach. If you take all of this into consideration you will 
                    most likely come to the conclusion that the probability of 
                    being targeted by a terrorist attack during FreedomCon is 
                    extremely low. </p>
                  <p><font color="#000066"><b> <a name="wc"></a>I have a big question 
                    about Freedomcon. How can you run World Championships? Has 
                    Decipher given you permission to hand out a champion title?<br>
                    </b></font><font size="3"><b><br>
                    </b></font><b> <font color="#FF0033">The FreedomCon World 
                    Championship level events are not recognized or supported 
                    by Decipher. </font></b> As you know Decipher cancelled their 
                    World Championship this year. However, many players that worked 
                    very hard to qualify for those events disagreed with that 
                    decision. <br>
                    <br>
                    Many in our community feel it is unfair to let that hard work 
                    and accomplishment go un-rewarded. FreedomCon is our attempts 
                    to give those players back a very important choice, a choice 
                    that was taken from them. </p>
                  <p>Are you going to let the terrorist frighten you into changing 
                    your life-style? That's why it's called terror-ism? They want 
                    us to give up what we love and enjoy and hide somewhere in 
                    fear. FreedomCon gives us the choice to demonstrate that as 
                    a community we will not let those terrorist bastards make 
                    us cower. You know what FreedomCon is? It's a way to say "Screw 
                    You", to the terrorist dogs that want us to hide in fear. 
                    <br>
                    <br>
                    Sure the title will not be "Star Wars CCG World Champion", 
                    rather it will be "FreedomCon World Champion - Star Wars", 
                    or trek, jk etc. But you know what? That title has more meaning 
                    behind it. <br>
                    <br>
                    It means that even without the thousands of dollars of corporate 
                    money or backing, the players of our community were able to 
                    pull together with their own funds and take a stand against 
                    terrorism. It means that they were able to organize a convention 
                    despite incredible odds, raise money for a good cause, and 
                    recognizing the players that deserve it. Is it an Official 
                    World Champion title? No. But it will be better than that. 
                    It is the championship title for the brave hearted gamer. 
                    <font color="#000066"><b><br>
                    <br>
                    <a name="venue"></a>Why keep it in Virginia? Why not Orlando 
                    or Vegas? </b></font><br>
                    <b><br>
                    Quick Answer: </b> Because the convention space was available, 
                    the price was right, and it represented the smallest change 
                    from the original convention, DecipherCon.<br>
                    <br>
                    <b>Complete Answer: </b> Honestly, Virginia Beach is not the 
                    best place to run a large gaming convention. It is definitely 
                    not the best place from the players point of view and if we 
                    had the time to plan the event out properly and had no extenuating 
                    circumstances then we would most certainly not have chosen 
                    Virginia Beach. </p>
                  <p>That being said, there are many reasons and unusual circumstances 
                    that resulted in it being the venue of choice for this convention. 
                    Some included, availability, price, and the least amount of 
                    change required by the players.</p>
                  <p>One of the ideas behind FreedomCon is that we should not 
                    let the terrorist have an additional victory by allowing them 
                    to rob us of our yearly convention. We could think of no better 
                    way to demonstrate this resolve than by having the convention 
                    at the same time and in the same place DecipherCon was scheduled 
                    to be held. </p>
                  <p>We understand that this might mean that many on the west 
                    coast will be unable to make it to FreedomCon. We are currently 
                    looking into plans to hold another large convention in Las 
                    Vegas some time next year. FreedomCon II! <br>
                    <br>
                    <br>
                    <font color="#000066"><b><a name="r3"></a>Will Reflections 
                    III be legal for the WC Events? </b></font><br>
                    <b><br>
                    Quick Answer: </b>Yes.<br>
                    <br>
                    <b>Complete Answer: </b>We have been following the debate 
                    concerning RIII and the FreedomCon World Championship with 
                    much interest. Here are some of the conclusions we have come 
                    to. <br>
                    <br>
                    The only substantial reason to disallow RIII is to increase 
                    the number of people that might be able to attend FreedomCon. 
                    After looking at Reflections III we believe that <b>it is 
                    possible to build competitive decks in a RIII environment 
                    that contain a mimimal amount or no Reflections III cards</b>. 
                  </p>
                  <p>To help people that would be unable to afford RIII and travel 
                    to FreedomCon, DeckTech will be running a &quot;Best Deck&quot; 
                    contest on decktech.net. This contest will be for the best 
                    designed non-RIII deck that can compete in a RIII environment. 
                    The strategy must contain the method to deal with the new 
                    objectives and mains. The winner of this contest will receive 
                    $100 and free entry into FreedomCon.</p>
                  <p>If you do not plan on buying Reflections III simply to protest 
                    Deciphers decision to cancel DecipherCon please consider this. 
                    The retailers have already placed their orders for Reflections 
                    III. By refusing to buy Reflection III you are hurting the 
                    retailers, and we really need to support our retailers during 
                    these times. If you are serious about protesting, call your 
                    retailer and tell him not to order any more Decipher products 
                    based on your anticipated purchases. This way our local shops 
                    can make adjustments that in no way hurt their business. <br>
                    <br>
                    <font color="#000066"><b><a name="organizing"></a>Who is organizing 
                    FreedomCon and who is making money from the event? </b></font><br>
                    <br>
                    <b>Quick Answer:</b> Players like you are organizing it. All 
                    proceeds go to the American Red Cross Disaster Relief Fund.<br>
                    <br>
                    <b>Complete Answer:</b> <br>
                    <br>
                    DeckTech is sponsoring FreedomCon, but it is a joint effort 
                    driven by many other quality tournament directors and players 
                    who simply want to do something to demonstrate their resolve 
                    in the face of the current adversity. <br>
                    <br>
                    We know there are good players who are out there who do not 
                    like DeckTech or care for some of the things we have done 
                    in the past. However, this event is not about DeckTech. It 
                    is about the players and a game they love to play. </p>
                  <p>It is about coming together in the face of adversity and 
                    doing something that was taken away from us. It is about coming 
                    together and doing something for the better good. <br>
                    <br>
                    As such we are determined to donate all proceeds to the American 
                    Red Cross Disaster Relief Fund. <br>
                    <br>
                    <font color="#000066"><b><font color="#000066"><b><a name="qualify"></a></b></font>I 
                    qualified for day 2 at DecipherCon, do I have to qualify again?</b></font><br>
                    We understand the level of dedication, work and the expense 
                    associated with qualifying for a competition as large as Deciphers 
                    World Championship. As such, FreedomCon will honor all such 
                    2001 qualifiers. <br>
                    <br>
                    If you qualified for a bye into day 2 of DecipherCon’s World 
                    Championship events for Star Wars, Star Trek, Young Jedi or 
                    Jedi Knights, you also qualify for a bye into the day 2 equivalent 
                    of FreedomCon’s World Championship events.<br>
                    <br>
                    <b><font color="#000066"> <b><a name="side"></a></b>Will there 
                    be other things to do besides play in the Championship Events? 
                    </font></b><br>
                    <br>
                    There will be many alternate events to play in. Stay tuned 
                    for the schedule of events as they become available.<br>
                    <br>
                    <b><font color="#000066"><b><a name="help"></a></b>How can 
                    I help?<br>
                    <br>
                    </font></b>There are several ways you can help. </p>
                  <ul>
                    <li>Come to FreedomCon! </li>
                    <li><a href="tell.php">Tell your friends</a> about FreedomCon 
                      and get them motivated to come!</li>
                    <li>Volunteer to help man the FreedomCon HQ or judge some 
                      events during the convention!</li>
                    <li>Donate cards or other prizes to give to the winners of 
                      the FreedomCon events!</li>
                    <li>Donate money to the <a href="donate.php">FreedomCon Fund</a> 
                      and be listed as a FreedomCon Fighter on the FreedomCon 
                      Weekend Planner!<br>
                      <br>
