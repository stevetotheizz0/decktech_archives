    <td width="700" background="/lotr/gfx/cloud/greencc.gif" height="15" valign="bottom"><table width="100%" border="0" cellspacing="0" cellpadding="0">
        <tr><td><font face="Tolk, Times, serif" class="h1font"><font color="#82A482">Direct Damage in the Lord Of The Rings TCG</font></B></font></td></tr></table></td></tr><tr><td width="2"></td><td valign="top"> 
      <table width="100%" border="0" cellspacing="0" cellpadding="5"><tr><td><font face="  Times, Times New Roman, serif" class="bfont"> By: <a href="http://www.decktech.net/lotr/main/mpage.php?member=8428">Marc 
            'Witch-King' THERY</a>. <br>
            (this article in available in <a href="dd_french.php">french</a>) 
            <p> <a href="http://www.decipher.com/lordoftherings/cardlists/fellowship/large/209bladetip.html" target="_blank"><img src="../gfx/blade_tipcard.jpg" width="200" height="240" align="right" border="0"></a>In 
              most CCGs, a common tactic to win is by inflicting 'direct damage'. 
              Direct damage means : 'damage they can't avoid'. In Lord Of The 
              Rings TCG (LOTR TCG), this tactic is available too, as we'll see 
              below. In this game, an injured character (especially an ally) won't 
              always be able to 'exert' and use his or her special game text. 
              You will also generally weaken (or simply get rid of) some major 
              companions to dissuade opponent from moving too fast (by steps of 
              2 sites in a single turn). Remember there are two kind of damages 
              opponent can receive in LOTR TCG : wounds and burdens. Both of them 
              may lead you to victory. 
            <p> Here are the ways to inflict wounds :<br>
              The most obvious means is by archery fire. Forget the fact you can't 
              really choose which of your opponent's characters will be hurt, 
              you deal damage and you even haven't to win a skirmish to do that! 
              So, cards enhancing your archery (or allowing you to choose your 
              target(s) ) are great. Here are some, both for the Free People Player 
              and the Shadow Player: 
            <p> Free People Player (FPP) :<br>
              Legolas, GreenLeaf (obvious). Card number (CN) : 50.<br>
              Aragorn's Bow => Aragorn, Greenleaf. :-) See Legolas. CN : 90.<br>
              Orophin or Rùmill (little less effectives). CNs : 56 & 57.<br>
              Double Shot (adds 1 to archery). CN : 38.<br>
              The Splendor Of Their Banners. (exert archer to add 1 to archery. 
              Useful on a character who isn't an archer by himself) CN : 62.<br>
            <p> Shadow Player :<br>
              Bitter Hatred (Spot an archer to wound an Elf, and it doesn't count 
              towards your archery total). CN : 164.<br>
              Orc Bowmen (very effective if you play Sauron Culture Orcs with 
              or without archers). CN : 264.<br>
            <p> Besides the archery, there are plenty of other cards to deal wounds. 
              Of course, the less cards you have to play (or to exert) to deal 
              the same damage, the best it is. 
            <p> FPP :<br>
              Here Lies Balin, Son of Fundin (wounds two orcs or one twice). CN 
              : 19.<br>
              Foes Of Mordor (exert a man companion to wound Sauron's minion ). 
              CN : 105<br>
              Blade Of Gondor (Boromir may now be useful :-) ). CN : 95.<br>
              Power According To His Stature ( to place in a Super-Frodo Deck 
              :-) ). CN : 308.<br>
            <p> SP :<br>
              Relentless Charge (wounds every archer if your Nàzgül is exerted). 
              CN : 223<br>
              Their Power Is In Terror (one of the best cards because ally generally 
              have a vitality of 2 and will be dead or won't be able to exert 
              anymore). CN : 226.<br>
              Hate (wound a companion of your choice). CN : 250<br>
              Ulaïre Enquëa, Lieutenant of Morgul. CN : 231.<br>
              Mordor's Strengh (Exert Sauron's Orc to wound a character). CN : 
              255.<br>
              Orc Hunters, Orc Scouting Band, Orc Soldier. CNs : 267, 270 & 271. 
              (They deal wounds during the skirmish if you exert them) 
            <p> Some cards, such as Saruman's Chills (CN : 134) are much more 
              vicious, because they force an opponent to exert one or more of 
              his characters. Most of them are sites (see below). 
            <p> The most powerful combo right now is to play Under The Watching 
              Eye (CN : 281) combined with many You Bring Great Evil (CN : 283) 
              or (if you play Nàzgüls) Black Breath (CN : 207) : whenever opponent 
              moves, he receive one wound and with YBGE, he cannot heal at sanctuaries 
              (not heal at all if you played Black Breath). 
            <p> You may deal up to eight wounds that way if you're lucky enough 
              to put UTWE in play from your opening hand. (Personnally, I play 
              4 of those cards) 
            <p> Some sites may also deal wounds. If this is your goal, let opponent 
              start and play those ones :<br>
              Midgewaters Marshes. (Every Hobbit must exert) CN : 332<br>
              Dwarrowdelf Chamber.(if your usual opponents don't play with Gimli) 
              CN : 344. Or Moria Lake (CN : 346)<br>
              Silverlode Banks. (Be sure to get rid of opponent's rangers before 
              he arrives there !) CN : 355.<br>
              Anduin Banks (adds a lot to archery). CN : 356.<br>
            <p> As opponent will be seriously wounded, play some strong minions 
              with damage +1, and you should kill a lot of companions without 
              to many difficulties. 
            <p> Of course you may instead choose to corrupt the Ring-bearer. 
            <p> Here are the means to proceed :<br>
              Forcing the Ring-bearer to put on The One Ring, and dealing him 
              wounds afterwards. You may force the Ring-bearer to put it on by 
              dealing him a lot of wounds or by playing special cards (only one 
              right now : Saruman's Reach CN 137).<br>
            <p> Play one of these cards (all of obvious use) : <br>
              Uruk Spy (CN 155) & Lost To The Goblins (CN 189), Worry (CN 162), 
              Cruel Caradhras (CN 124), Drawn To Its Power (CN 211), In The RingWraith's 
              Wake (CN 214), Goblin Spear (CN 182). 
            <p> But the best cards to add burdens are :<br>
              1. Desperate Defense of The Ring (CN 244), because you usually play 
              it on opponent's most powerful character, generally Aragorn, Gandalf 
              or Legolas, and if the FPP assigns too often this character to the 
              skirmishes, he will add a bunch of burdens.<br>
              2. Thin and Stretched (CN 279). Play cards to slow down the FPP, 
              and play only a few powerful minions at each sites : the FPP will 
              be annoyed a lot.<br>
              3. Blade Tip (CN 209) may also add burdens if played on the Ring-bearer. 
              But the odds are few you'll play on him.<br>
            <p> To conclude, I would say that methods to inflict direct damage 
              are more readily available for the Shadow Player than for the Free 
              People Player, so direct damage becomes more relevant if you are 
              playing a deck that primarily attempts to kill or corrupt Frodo 
              rather than focuses on getting your own Frodo to site nine. 
            <p> Marc 'Witch-King' THERY. " You're brave, little man… But your 
              courage alone will not save you ! " </font></td></tr></table></td></tr></table> 
