<h3>Questions about Card Rulings:</h3><br />
<b>THX for the update... I have one question that whoever that knows the right answer can answer to me dirrectly. The card in question is Blood Runs Chill. when i for example spot a dwarf with damage+5 and make an opponent discard 5 shadow cards, can the shadow player discard shadow cards from his hand also? This came up when i blew my friends game by playing BRC three times on his sauron wounding grind setup and he protested that he could discard cards from his hand too so he wouldn’t loose all his pretty conditions. Any advice is much appreciated! Thanks! (+3 tp) [masquerade]</b><br />
<br />
A: You can only discard cards from play.<br />
<br />
The default definition of discard is &quot;discard from play&quot;. So Blood Runs Chill requires a Shadow player to discard cards that are in play. This does not include cards from hand, on top of their draw deck, or stacked.<br />
<br />
<b>Hey, I always agree with your rulings, so I was wondering if you could clarify this way around Runs Chill for me. Can you use the Goblin card Plundered Armories. Because BRC tell you to discard X shadow cards. And you discard it, then you get to respond, with PA, right? So you discard it, then it comes right back, so you can discard it again, right. [Graham]</b><br />
<br />
A: Yes.<br />
<br />
Argh! Someone has figured out my tech! Blood Runs Chill makes the Shadow player discard X cards. Regardless of what X equals, the discarding is done one at a time. If you choose to discard a [Moria] weapon, you can react with Plundered Armories to play it again before the next discard. And since it’s back in play, you could discard it again! Oh well, cat’s out of the bag. )=<br />
