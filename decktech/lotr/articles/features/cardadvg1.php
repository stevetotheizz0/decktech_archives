		<td bgcolor="#FFFFFF" background="/lotr/gfx/mom/whi_08.gif"><font class="bfont"> 
            <p> By: <a href="http://www.decktech.net/lotr/main/mpage.php?member=3604">Mark 
              "Rogue Nine" Balderston</a><br>
              <br>
              In every game, there are assets available to a player and his opponent. 
              In Monopoly there is Monopoly money, In Star Wars there is simply 
              the Force (of course it is much more complicated than that), and 
              in Lord of the Rings there are two major assets: cards available 
              to the player and, more obviously, the twilight pool. The twilight 
              pool is simpler to manipulate and exploit, but card advantage is 
              just as if not more important. Card advantage is the focus of this 
              article series. You can manipulate the relationship between the 
              hand and twilight pool, but I'll save that topic for another article. 
            <p> <b>What is a CARD ADVANTAGE and why is it important?</b> 
            <p> There are three ways to have a card advantage. One: you have more 
              cards in your hand than your opponent. Two: You have more (and hopefully 
              better) useful cards on the table than the opponent. Three: You 
              have the ability to make cards available to you faster and in greater 
              numbers than your opponent does. All of these also work in reverse, 
              if you deny your opponent cards then you have the advantage, or 
              if you deny him the ability to play cards and cycle fresh cards 
              into his hand then that is also a card advantage. I will go into 
              more detail on this later. 
            <p> Why is it important to have a card advantage? Here is a very, 
              very simple gameplay situation. 
            <p> I have Gandalf, the Grey Wizard with Glamdring. My opponent has 
              an Orthanc Assassin. They skirmish and I play Servant of the Secret 
              Fire. My opponent passes his action and Gandalf wins the skirmish, 
              wounding the Orthanc Assassin twice. 
            <p> In this example, I used three cards to the one that my opponent 
              used. I won the skirmish because I had a card advantage. Get the 
              idea? Granted, many times this does not hold true. For example if 
              your opponent has eight pipeweeds and four bounders on the table, 
              but no hobbits (besides Frodo) or pipes, then I would have to say 
              that this is probably NOT a card advantage. 
            <p> Of course you have to make efficient use of your cards, but this 
              is another topic entirely, and it is fairly easy to do. Whichever 
              player can continually be re-supplied with useful cards and keep 
              cards in his or her hand has an advantage. There are many ways to 
              gain and then exploit this advantage. 
            <p> <b>Cycling cards in and out of your hand:</b><br>
              Now this is not really an abstract concept. With Lord of The Rings, 
              the more cards you play during your turn, the more cards you can 
              draw. This is good. Let's define some theoretical card types in 
              order to understand some basic principles behind card advantage. 
              First we should define something called a Permanent. 
            <p> <b>PERMANENT</b> <br>
              Now a permanent is something that is played and stays on the table 
              past the end of the regroup phase. This would be a condition, character, 
              or possession. The more permanents in your hand, the more cards 
              you will be able to play and thus the more you will draw at the 
              regroup. This is not to say that having non-permanent cards in your 
              hand is necessarily bad. Not by any means. Let's define another 
              type of card, and let's call it A situational. 
            <p> <b>SITUATIONAL</b> <br>
              A situational card would be a card that can only be played in certain 
              situations. Makes sense, doesn't it? The Seen and the Unseen would 
              be a situational card because your opponent must have a condition 
              on the table and you must have an elf that is able to exert. Situational 
              cards are not always bad, but when constructing a deck, make sure 
              that these situations will occur fairly often, or that the card 
              has another worthwhile function. This is VERY important because 
              a situational card that CANNOT be played will sit in your hand until 
              you can play it or until you have the opportunity to discard it. 
            <p> The twilight cost of minions would be considered a situation, 
              and should take much consideration in deck construction. Granted, 
              you cannot play shadow cards in the Free Peoples turn, and vice 
              versa. Therefore construct a deck that will play 4-7 cards a turn 
              and run well off this amount of cards. A deck that cannot play cards 
              cannot draw cards and is in deep trouble. Therefore, a deck with 
              high-cost minions, like Nazgul needs to balance the card consumption 
              by using events and cheap conditions, otherwise cards that cannot 
              be used will stagnate in the hand. 
            <p> By denying your opponent twilight pool, you prevent him from playing 
              cards. This gives you, a card advantage. Vice versa, if you play 
              many cards and leave the opponent with more pool than he can handle, 
              you also have a card advantage because then you have that many more 
              cards to deal with his. I think that the most dangerous ground here 
              is, the middle! 
            <p> This covers the introduction to card advantage; the next article 
              will cover more specific ways to gain the card advantage and risk 
              vs. reward in getting it.<br>
