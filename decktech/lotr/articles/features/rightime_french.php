		<td bgcolor="#FFFFFF" background="/lotr/gfx/mom/whi_08.gif"><font class="bfont"> By: <a href="http://www.decktech.net/lotr/main/mpage.php?member=8428">Marc 
            'Witch-King' THERY</a> 
            <p> <img src="../../gfx/features/elrond.gif" width="200" height="240" align="right">A 
              LOTR TCG, et au contraire d'autres jeux de cartes, tels que SWCCG, 
              il y a une limite au nombre de cartes que l'on peut avoir en main 
              à un moment donné. A la fin de chaque tour, on ne peut pas avoir 
              plus de huit cartes en main. Donc, comment faire en sorte d'avoir 
              la bonne carte quand on en a besoin ? Si on a cette carte, mais 
              qu'on ne peut l'utiliser sur l'instant, est-ce utile de la conserver 
              indéfiniment en main, ce qui réduit la main à sept autres cartes 
              ? Y a-t-il des moyens de récupérer cette carte si on choisit de 
              la défausser ? Nous allons essayer d'examiner quels peuvent être 
              ces moyens. 
            <p> On peut diviser ces moyens en trois catégories : pioche, récupération 
              et recherche. Les systèmes de pioche permettent de tirer plusieurs 
              cartes jusqu'à ce qu'on trouve ce que l'on cherche. C'est le moyen 
              le plus évident et le plus simple, mais aussi le plus mauvais, car 
              il faut généralement défausser aussi beaucoup, ce qui occasionne 
              la perte de bonnes cartes (toutes les cartes du deck méritent d'y 
              être, non ? :-) ). Les systèmes de récupération permettent de récupérer 
              (sic) quelque cartes dans le draw deck à partir de la pile de défausse, 
              ce qui accroît les chances de les rejouer par la suite. C'est très 
              efficace quand il ne reste plus que quelques cartes dans le draw 
              deck (c'est à dire en fin de partie). Enfin, les systèmes de recherche 
              permettent de prendre une carte particulière de la pile de défausse 
              ou du draw deck et de la jouer immédiatement. Ce sont souvent les 
              meilleures mécaniques de jeu. 
            <p> Nous allons étudier ces trois catégories, à la fois pour le joueur 
              Free People et le joueur Shadow. Quand on veut une carte, il est 
              possible de piocher et défausser comme un fou jusqu'à ce qu'on l'ait. 
              Quelques moyens existent pour tirer plusieurs cartes par tour. Le 
              joueur Free People utilisera souvent Elrond, Lord Of Rivendell qui 
              permet un tirage maximal de trois cartes le premier tour, puis une 
              par phase de fellowship. Elrond est particulièrement bon pour cet 
              usage si on l'utilise avec la combo Galadriel / Celeborn qui permet 
              de le soigner plusieurs fois par tour, et/ou avec Ottar, Man of 
              Laketown, qui peut défausser les cartes inutiles tirées de cette 
              manière. Egalement, on peut combiner Elrond avec une combo Delving 
              / Grimir, Dwarven Elder. Cette dernière combo est très utile pour 
              se préparer une main Shadow : on pioche des cartes, on défausse 
              les cartes Free People et au prochain tour on en récupére qielques 
              unes aves Grimir. Pour tirer des cartes, il y a d'autres moyens 
              tels que : Silinde, Elf of Mirkwood, quand il a le game text de 
              Rivendell Valley, ou la combo Bilbo's Pipe / Old Toby / Trollshaw 
              Forest. Le joueur Shadow n'a que peu de moyens pour piocher : à 
              part Threat of the Unknown (pas très bonne, car il faut exerter 
              un orc de la Moria), Bent on Discovery (meilleure : on n'exerte 
              que si on le veut) et Seeking Its Master (la meilleure des trois), 
              il n'y a aucun moyen. 
            <p> Quand il s'agit de récupérer des cartes dans l'espoir de les rejouer, 
              il n'y a que peu de méthodes. Le joueur Free People ne peut récupérer 
              des cartes qu'avec Barliman Butterbur (un événement de la culture 
              Gandalf), Grimir (plus utile) ou la combo : Bilbo Baggins, Retired 
              Adventurer et Frodo, Old Bilbo's Heir (très bonne combo pour récupérer 
              le précieux Power According To His Stature :-) ). Les options du 
              joueur Shadow ne sont pas plus nombreuses : si l'on excepte Breeding 
              Pit et le Morgul Skullker, des cartes idéales pour récupérer des 
              adversaires clés, il n'y a que la combo Goblin Sneak / The Underdeeps 
              of Moria, qui permet de placer un orc sous le draw deck et ensuite 
              de l'utiliser. Récupérer des cartes est utile, mais mieux vaut ne 
              pas en dépendre entièrement. 
            <p> Enfin, il y a les cartes de recherche, qui autorisent la recherche 
              et/ou la mise en jeu de certaines cartes de la défausse ou du draw 
              deck, ou qui permettent de révéler des cartes et d'en garder un 
              certain nombre en main. Ces cartes sont, à mon humble avis, les 
              plus utiles. Remarquez que la plupart sont des cultures Gandalf 
              et Shire : Questions That Need Answering, Risk A Little Light, A 
              Wizard Is Never Late (pour prendre un allié ou Gandalf) sont utiles. 
              Master Proudfoot ou the Hobbit Farmer combiné à Farmer Maggot's 
              Field, Bag End, Green Dragon Inn, The Prancing Poney, ou Shire Lookout 
              Point peuvent également aider. La culture naine n'a que deux cartes 
              de recherche : Mithril Shaft, pas très bonne car vous ajoutez à 
              la réserve crépusculaire (et c'est toujours mauvais), et Wealth 
              of Moria. En ce qui concerne les elfes, seul The Council of Elrond 
              existe, mais il peut prendre quelques cartes précieuses comme The 
              Tale Of Gil-Galad ou The Last Alliance Of Elves and Men. Le joueur 
              Shadow a des moyens efficaces de recherche, mais uniquement pour 
              les cultures Moria ou Ringwraith. Les Nazguls ont The Master's Will 
              et Ulaire Nertea (une star), tandis que les orcs ont Fool of a Took!, 
              They Are Coming ou la combo bien connue Host of Thousands / Goblin 
              Scavengers / Goblin Scimitar. Ne parlons pas des Uruk-Hai ou des 
              orcs de la culture Sauron.Il n'y a rien pour eux. 
            <p> Pour conclure, je dirai qu'il y a plus de mécanismes de pioche 
              que de vrais mécanismes de recherche pour le joueur Free People. 
              Pour le joueur Shadow, seuls les orcs de la Moria peuvent réellement 
              chercher ce dont ils ont besoin, mais seulement des cartes qui ont 
              déjà été jouées. Les futures extensions du jeu introduiront certainement 
              des moyens plus efficaces.
